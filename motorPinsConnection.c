// === MOTOR PINS ===
// [Back Left]
#define FBL 18 // Forward Back Left
#define BBL 17 // Backwards Front Left
#define PWM_BL 5 // PWM Back Left

// [Back Right]
#define FBR 3 // Forward Back Right
#define BBR 8 // Backwards Back Right
#define PWM_BR 9 // PWM Back Right

// [Front Right]
#define FFR 13 // Forward Front Right
#define BFR 14 // Backwards Front Right
#define PWM_FR 10 // PWM Front Right

// [Front Left]
#define FFL 11 // Forward Front Left
#define BFL 12 // Backwards Front Left
#define PWM_FL 16 // PWM Front Left

// STBY pins
// When STBY is 0, the motor drivers are off (will not work)
// Have to turn STBY to 1 to be able to control the motors
#define STBY1 47
#define STBY2 21
